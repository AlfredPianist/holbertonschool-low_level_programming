/**
 * add - Sums to numbers.
 * @a: First term.
 * @b: Second term.
 *
 * Result: a + b.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Subtracts to numbers.
 * @a: First term.
 * @b: Second term.
 *
 * Result: a - b.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiplies to numbers.
 * @a: First factor.
 * @b: Second factor.
 *
 * Result: a * b.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Divides to numbers.
 * @a: Dividend.
 * @b: Divisor.
 *
 * Result: a / b.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - The modulo of two numbers.
 * @a: Dividend.
 * @b: Divisor.
 *
 * Result: a % b.
 */
int mod(int a, int b)
{
	return (a % b);
}
